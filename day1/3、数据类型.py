# 数据类型 int、float、bool、list、dict

# int
age = 18
print(age, type(age))

# float
salary = 8888.9
print(salary, type(salary))

#bool: True/False
result = False
print(type(result))

# list: 存放多个数据
list_1 = ['tom',  'jerry', 'daven']
print(list_1, type(list_1))

# list嵌套
list_3 = [
    ['tom', 'cat'],
    ['mm', 'dd'],
]
print(list_3, type(list_3))

# dict: key-value
dic= {
    'name': 'daven',
    'age': 18,
}
print(dic, type(dic))